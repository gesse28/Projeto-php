        <p class="paragrafo">1950 - Na década de 1950, ao desenhar o computador Mark III, Howard H. Aiken desenvolveu um modo para traduzir em códigos numéricos os comandos em notação matemática preparados por um programador.Maurice V. Wilkes, usou o Assembler (linguagem simbólica Assembly) no EDSAC, na Universidade de Cambridge.Ainda nesse ano a "Remington-Rand" adquiriu a "Eckert-Mauchly Computer Corp". Por essa época, Grace Hopper transformou-se em uma pioneira no processamento de dados, criando o primeiro compilador, Trata-se de um programa que possibilita transformar o código de um programa, escrito em uma linguagem de programação, como C, Cliper, Python.., em linguagem de máquina, gerando o código binário o qual efetivamente pode ser executado pelo computador.</p>
